    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        String username = txtTenDN.getText();
        String password = txtMatKhau.getText();

        try {
            if (validateLogin(username, password)) {
                JOptionPane.showMessageDialog(null, "Đăng nhập thành công!");
                MainBanGiay kh1 = new MainBanGiay();
                kh1.setVisible(true);
                
                this.dispose();
                // Perform actions after successful login (e.g., open main application window)
            } else {
                JOptionPane.showMessageDialog(null, "Tên đăng nhập hoặc mật khẩu không đúng!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(DangNhapGui.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Lỗi kết nối database!");
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed


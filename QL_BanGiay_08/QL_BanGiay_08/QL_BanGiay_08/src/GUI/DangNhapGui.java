 private Connection getConnection() throws SQLException {
        String user = "sa"; // Replace with your actual username
        String password = "123"; // Replace with your actual password
        String url = "jdbc:sqlserver://localhost:1433;" + "databaseName=QL_BAN_GIAY_09; trustServerCertificate = true";
        return DriverManager.getConnection(url, user, password);
    }

    private boolean validateLogin(String username, String password) throws SQLException {
        String sql = "SELECT * FROM TaiKhoan WHERE TenDN = ? AND MatKhau = ?";
        try (Connection con = getConnection(); java.sql.PreparedStatement ps = con.prepareStatement(sql)) {
            ps.setString(1, username);
            ps.setString(2, password);
            java.sql.ResultSet rs = ps.executeQuery();
            return rs.next();
        }
    }
    
